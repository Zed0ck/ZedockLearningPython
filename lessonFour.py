# We use keyword def to let know Python we were starting a function
# You will give any name  you want no need to be "add"
def add(n1,n2):
    print("The answer is: {} ".format(n1+n2))
add(4,2)

def subract(n1,n2):
    print("The answer is: {} ".format(n1-n2))
subract(4,2)

def multiply(n1,n2):
    print("The answer is: {} ".format(n1*n2))
multiply(4,2)

def divide(n1,n2):
    print("The answer is: {} ".format(n1/n2))
divide(4,2)

def greeting(name):
    print(f"Hi {name}")
greeting("Jailani")
